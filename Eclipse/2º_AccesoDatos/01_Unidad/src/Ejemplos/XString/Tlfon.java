package Ejemplos.XString;

public class Tlfon {
	private int codigo;
	private int numero;
	
	public Tlfon(int codigo, int numero) {
		this.codigo = codigo;
		this.numero = numero;
	}
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
	
}
